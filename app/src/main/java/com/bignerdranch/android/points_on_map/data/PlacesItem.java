package com.bignerdranch.android.points_on_map.data;

public class PlacesItem{
	private double lng;
	private String name;
	private int id;
	private double lat;

	public double getLng(){
		return lng;
	}

	public String getName(){
		return name;
	}

	public int getId(){
		return id;
	}

	public double getLat(){
		return lat;
	}
}
